#include "../headers/libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s == (char)c || *s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
