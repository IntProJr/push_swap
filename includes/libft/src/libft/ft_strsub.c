#include "../headers/libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*new;
	size_t	i;

	if (!s || start + len > ft_strlen(s))
		return (NULL);
	new = ft_strnew(len);
	if (!new)
		return (NULL);
	i = 0;
	while (i < len)
		*(new + i++) = *(s + start++);
	new[i] = '\0';
	return (new);
}
