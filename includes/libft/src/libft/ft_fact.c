#include "../headers/libft.h"

int	ft_fact(int n)
{
	int x;
	int y;

	x = 1;
	y = 1;
	if (n < 0 || n > 12)
		return (0);
	if (n > 1)
	{
		while (x <= n)
		{
			y = y * x;
			x++;
		}
	}
	return (y);
}
